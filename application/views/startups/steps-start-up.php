 <h5>For a Start-up you must be ready with the below mentioned 8 things first;</h5>
 
 <h5>Have an Idea</h5>
 <p>
Idea is the most important aspect of a Start-up. It is the base on which a business will run. If you are confident that you can pull off something special and you like it then you must go for it.</p>

<h5>Complete Market / Industry Research</h5>
 <p>
This is a very important stage for any business whether a Start-up or not. Industry / market research is a must to analyze the viability of the business. There are many questions that will rise and have to be answered in order to get refined and pass this stage.</p>


<h5>Choosing the right business structure</h5>
 <p>
As a Start-up you will have to understand the dynamics of the right business structure. You can’t start as a company but yes as a Sole Proprietor or as a business partner. This is very important as it involves lot of legal issues.</p>

<h5>Create a detailed Business Plan</h5>
 <p>
A detailed business plan is required as this showcases your business identity and idea. This document involves all the detailed financial projections with all relevant information. This is the document that has an impact on the investor at the first instance.</p>


<h5>Effective and Creative naming to the business</h5>
 <p>
You can realize from facebook, Zomato, flipkart, Whatsapp etc… that their names are unique, creative and innovative. A business will be a success or a failure will be according to the name which needs to be a memorable one. Yu should be very careful and should have a fully refined name to your business.</p>


<h5>Out of the box designed logo</h5>
 <p>
Impressive and well designed logo is the first impression that a prospective customer has of your business. You must take care of this step in particular with all finishing done in a precision manner. Remember your business name & logo will remain with you everywhere.</p>


<h5>Build a user friendly website</h5>
 <p>
Today all are taking full advantage of the internet and the social media. You must create a user friendly website and market it creatively on the social media circuits to enhance the visibility of your idea and business.</p>

<h5>Get your finances in right order</h5>
 <p>
For all the above mentioned steps you need to have the right finance structure ready to proceed.  You need to answer the following questions in this step;</p>


<ol>
<li>What percent of investment is Outside Debt</li>
<li>How frequently you will be planning your finance</li>
<li>How to keep your costs as low as possible</li>
<li>When do you feel you require the next round of funding and how  to utilize it </li>

</ol>






