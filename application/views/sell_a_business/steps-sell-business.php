  <h5>Steps involved in Selling a Business</h5>
  <p>Business is set-up not thinking of an exit and there is lot of emotions and feelings attached with it, but there are times when one needs to think strategically and realistically to move ahead rather than sitting on fire. Whether or not you profit will depend on the reason for the sale, the timing of the sale, the strength of the business operation and its structure. The business sale process would also require much of your time and energy. Selling a business is a complex venture that involves several considerations some of which can help you build a solid plan and make negotiations a success.</p>
  
  <h5>1. Reasons for the Sale</h5>
  <p>You have decided to sell your business. Why? That's one of the first questions a potential buyer will ask. Owners commonly sell their businesses for any of the following reasons:</p>
  
  <ol>
  <li>Retirement</li>
  <li>Partnership & Family disputes</li>
  <li>Illness and death</li>
  <li>Becoming overworked</li>
  <li>Over-leveraging from banks and financial institutions.</li>
  <li>Forced acquisition by a buyer.</li>
  <li>No succession plan in place.</li>
  <li>Relocation and transfer</li>
  <li>Change his business line.</li>
  <li>The business performance is low.</li>
  <li>Shortage of funds.</li>
  </ol>
  <p>
  It would be advisable to be transparent with the buyer as it would give confidence to him to work out a plan to take up the business. The buyer needs to get the right information from you rather than from others during the course of transfer of ownership, which sometimes does not go well with the closing of the transaction.
  </p>
  </div>
  <div class="col-md-12">
  <h5>2. Timing of the Sale</h5>
 <p>
As soon as you take an internal decision to sell your business do not lose time to start the process of preparation for sale. Prepare for the sale as early as possible, preferably a year or two ahead of time. The preparation will help you to improve your financial records, business structure and customer base to make the business more profitable. You need to put a succession plan in place and all departments in your organisation need to be manned and not miss your presence in the company.These improvements will also ease the transition for the buyer and keep the business running smoothly. Some owners consider selling the business when it is not profitable, but this can make it harder to attract buyers. Consider the business's ability to sell, its readiness and your timing. There are many attributes that can make your business appear more attractive, including:</p>

<ul>
<li>a) Increasing profits</li>

<li>b) Consistent income figures</li>

<li>c) A strong customer base</li>

<li>d) A major contract that spans several years</li>

</ul>   
  </div>
  
  
  <div clas="col-md-12">
  <h5>3. Preparing your Sale</h5>
 
<p>Selling the business is as difficult and complex as starting a new business. So you must be fully prepared to support and justify your statements.
</p>
<p>
Now the question arises how you get yourself ready?
</p>

<ol>
<li>
Get your books in order
All the books must be complete with information displaying true and fair results. There must not be any ambiguity in any manner so as to create a smooth passage towards right sale.</li>
<li>
Organise your legal paperwork
Review your incorporation papers, permits, licensing agreements, leases, customer and vendor contracts, all bank and debt sanctions etc. Make sure you have them readily available, current and in order.Financial statements and all forms of Tax Returns dating back three to four years and review them with an accountant.</li>

<li>
Operating Manual
Your information packet should also provide a summary describing how the business is conducted and/or an up-to-date operating manual.
</li>

<li>
List of Assets
A consolidated list of Plant and Machinery, Equipment and other assets that are being sold with the business needs to be prepared giving the year of purchase and the condition of the same.
</li>

<li>

Get your Business Valuation
Now you'll want to determine the worth of your business to make sure you don't price it too high or too low. You must and try to have a valuation estimate of your business so as to assess your market or industry position with all financial implications. Locate a business appraiser to get a valuation. The appraiser will draw up a detailed explanation of the business's worth. The document will bring credibility to the asking price and can serve as a gauge for your listing price.
</li>
</ol>
  
  </div>
  
  
  <div class="col-md-12">
  <h5>4. Appoint a Business Broker</h5>
<p>
As a first it requires that a Business owner enlists a Business Advisor who acts as a combination of a Business Broker, Financial Accountant, lawyer and your guide to complete the transaction. Do not approach for sale without appointing a broker who can help free up time for you to keep the business up and running.The Business broker needs to be tutored about the facts of the business and transparency is essential.These intermediaries provide the right ingredient to support your sale. They help in the following ways;
</h5>

<ol>

<li> Provide in-depth insights on valuation, marketing, negotiations and other fundamental sales elements</li>
 
<li> Understand the art of getting multiple qualified buyers to the table </li>
 
<li> They know how to overcome all the obstacles that often prevent a sale from closing </li>
 
<li> They preserve confidentiality during the sale and marketing process </li>
 
<li> You Advertise with them in social media, websites and Newspapers </li>
 
<li> Insist on the Business Brokerâ€™s team needs to pay a visit to the business and get to first- hand information of the business. Once he has a feel of the business he would be in a better position to pitch for your sale.
 </li>
  
  </ol>
  </div>
  
  
  <div class="col-md-12">
 <h5> 5. Investor Memorandum (IM)/ Business Presentation</h5>
 
 <p>
This is a document which showcases your business to potential buyers. The buyer has his first encounter with this all important document and makes the overview of his judgement based on it. Business Presentation is not limited to specifics but is for all. The following detailing needs to be included in a Business Presentation;
</p>
<ol>
<li> Business Overview</li>
<li>Infrastructure Details</li>
<li>Product & Services </li>
<li> Sales & Distribution Network </li>
<li> Financials for 3 years and projections </li>
<li> Business Valuation</li>
</ol>
  
  </div>
  
  
  
   <div class="col-md-12">
 <h5> 6. Finding a Buyer</h5>
 
 <p>
A business sale may take between six months and twenty four months as this is a standard time required to sell a business and finding the right buyer can be a challenge. Do not limit your advertising since you would be getting new leads with each advertisement and you'll attract more potential buyers. Your Business Broker is your guide in this process and you need to have trust in him and his ability to get you the right buyer. Once you have prospective buyers, keep the processes moving along:
</p>
<ol>
<li> Get two to three potential buyers just in case the initial deal falters.</li>
<li>Ask the broker to stay in contact with the potential buyers.</li>
<li>Find out whether the potential buyer prequalifies for financing before giving out information about your business. If you plan to finance the sale, work out the details with an accountant or lawyer so you can reach an agreement with the buyer.</li>
<li>Allow some room to negotiate, but stand firm on the price that is reasonable and considers the company's future worth.</li>
<li>The potential buyers should sign a nondisclosure/confidentiality agreement to protect your information.</li>
<li> On completion of the negotiation sign a Term Sheet which forms a base for drafting a Share transfer Agreement.</li>
</ol>

<p>In addition, the buyer may have you sign a non-compete agreement, in which you would agree to not start a new, competing business and woo away customers.</p>
  
  </div>
  
  
  <div class="col-md-12">
 <h5> 7. Confidentiality</h5> 
 <p>
Confidentiality needs to be maintained as it is the key to the sale process and is necessary that the information of your sale does not leak out to your employees, customers, vendors and other associates including banks and financial institutions. Your entire Business Information should typically be provided to interested parties once they have signed a non-disclosure agreement</p>
  
  </div>
  
  
   <div class="col-md-12">
 <h5>8. Negotiations</h5> 
 <p>
This is a very challenging step to deal with the expectations of the seller and match with that of the buyer so as to complete the negotiation process. Here the intermediary helps in creating that positive atmosphere and let both seller and buyers understand the implications of the sale.
This is the step dealing with numbers so it is imperative to deal with it carefully.</p>
  
  </div>
  
  <div class="col-md-12">
 <h5>9. Closing the Deal</h5> 
 <p>
This is the final stage in the process of selling a business. Your Business Intermediary will again manage all aspects to expedite this process. Lawyers and accountants co-ordinate from both sides to ensure a timely and successful conclusion. All the owner has left to do is sign a few papers and meet their obligations under the terms. On completion of the negotiation sign a Term Sheet which forms a base for drafting a Share transfer Agreement.</p>
  
