<ul>
<li>First identify the objective of JV.</li>
<li>Write down the outcome of JV.</li>
<li>Analyze the target segment where you want to reach.</li>
<li>Identify the potential partner who already in the same segment.</li>
<li>Than DO SWOT analysis of those prospects and firm.</li>
